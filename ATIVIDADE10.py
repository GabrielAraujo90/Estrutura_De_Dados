def calcular_media(lista_numeros):
    soma = sum(lista_numeros)
    media = soma / len(lista_numeros)
    return media

# lista com o exemplo
numeros = [10, 20, 30, 40, 50,]

# calcular a média
media = calcular_media(numeros)
print("A média dos números na lista é:", media)


